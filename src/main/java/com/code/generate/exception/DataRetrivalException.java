package com.code.generate.exception;

public class DataRetrivalException extends Exception {

	public DataRetrivalException() {
		super();
	}

	public DataRetrivalException(String message, Throwable cause, boolean enableSuppression,
			boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);
	}

	public DataRetrivalException(String message, Throwable cause) {
		super(message, cause);
	}

	public DataRetrivalException(String message) {
		super(message);
	}

	public DataRetrivalException(Throwable cause) {
		super(cause);
	}

}
